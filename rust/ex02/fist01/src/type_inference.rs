// 타입 추론은 컴파일러가 변수의 타입을 명확한 타입 주석 없이도 유추하는 기능
// 러스트에서 대부분의 경우 컴파일러가 변수의 타입을 자동으로 추론할 수 있음
// 때로는 타입을 명시적으로 지정해줘야 할 필요가 있음
// 1. 변수 선언시 초기화하지 않은 경우
// 2. 여러 타입이 가능한 상황에서 명확히 하고 싶은 경우
// 3. 함수의 반환 타입을 명시적으로 지정하고 싶은 경우
// 타입 주석의 기본 문법은 변수명 뒤에 콜론과 타입을 명시하면 됨

pub fn main() {
    // 타입 추론을 이용한 변수 선언
    let x = 10;
    println!("x의 값: {}", x);

    // 타입 주석을 사용한 변수 선언
    let y: f64 = 3.14;
    println!("y의 값: {}", y);

    // 초기화 없이 변수 선언 후 타입 주석과 함께 값 할당
    let z: String;
    z = String::from("러스트 프로그래밍");
    println!("z의 값: {}", z);
}
